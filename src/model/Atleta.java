package model;

public class Atleta {
	public int cod;
	public String codp;
	public String nome;
	public String sexo;
	public int getCod() {
		return cod;
	}
	public void setCod(int cod) {
		this.cod = cod;
	}
	public String getCodp() {
		return codp;
	}
	public void setCodp(String codp) {
		this.codp = codp;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getSexo() {
		return sexo;
	}
	public void setSexo(String sexo) {
		this.sexo = sexo;
	}
	
	
}
